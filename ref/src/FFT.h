#ifndef _miniPi_FFT_H
#define _miniPi_FFT_H

#include <stdint.h>
namespace Mini_Pi{
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

//  Ensure that the pre-computed twiddle factor table is large enough to handle
//  a product size of CL. This method is not thread-safe with "multiply_FFT()".
void ensure_FFT_tables(size_t CL);

//  Multiply A by B and store into C.
//      -   AL and BL are the lengths of A and B.
//      -   AL + BL is the length of C.
//      -   A, B, and C point the start of a little-endian array big integer.
void multiply_FFT(uint32_t* C, const uint32_t* A, size_t AL, const uint32_t* B, size_t BL);

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
}

#endif
